import time



class Player(object):

    def __init__(self, surname, birthdate, firstname=None): 
        self.surname = surname
        self.birthdate = time.strptime(birthdate, '%d %b %Y')
        self.firstname = firstname

    def __str__(self):
        s = self.surname
        if self.firstname:
            s += '(%s)' % self.firstname
        return s

    def check(self, surname, firstname=None): 
        if not self.surname == surname: 
            return False
        if firstname: 
            if not self.firstname == firstname: 
                return False
        return True



